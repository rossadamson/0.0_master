                        <p>Recently, the company has streamlined their production methods a little, “so we can make our products more consistent and accessible to a wider audience,” says Groves and has switched to working with a different species of timber: Beech (fagus sylvatica).</p>

                        <p>“We feel [it] better suits our cleaner approach,” says Groves. “Beech is a really interesting species as it has naturally anti-bacterial properties which is obviously ideal for food based applications and also fits with our ethos of working with nature and spreading the word about the incredible little bits of magic occurring in the natural world every day that most people are unaware of.”</p>

