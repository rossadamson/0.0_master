                        <p>“The aim of all our workshops is to encourage fellow illustrators, designers and other creative folks to get away from their computer screens and tablet and spend a day in the woods learning about nature and making things with traditional tools in a relaxed creative environment,” says <a href="http://www.andrewgroves.co.uk/" class="link-style">Andrew Groves</a>, the illustrator and designer turned woodsman who founded <a href="http://miscellaneousadventures.co.uk/" class="link-style">Miscellaneous Adventures</a> in 2012 – later joined by his wife, the illustrator <a href="http://cargocollective.com/emmaruth/About" class="link-style">Emma Hughes</a>.</p>

