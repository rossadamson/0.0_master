                        <p>“I’ve also learnt so much about nature, trees and wood as a material through using one and want to share some of my knowledge with others. As a brand that focuses on creativity in the great outdoors, we’ve seen axes get more popular as US outdoor brands like Best Made Co. and others have made them appealing aesthetically.”</p>

                        <p>Groves also cites the popularity of “the creative outdoor scene” and the emergence of brands such as <a href="http://www.polerstuff.com/" class="link-style">Poler Stuff</a> and books like <a href="http://shop.gestalten.com/outsiders.html" class="link-style">The Outsiders</a> (Gestalten) and <a href="http://cabinporn.squarespace.com/" class="link-style">Cabin Porn</a> (Particular) as meaning more and more people are getting hold of axes for the first time – and not necessarily knowing how to use them properly.</p>

